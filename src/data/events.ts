import type { BoardEvent } from '../types/board';

function ev(
  iso: string,
  title: string,
  cal: BoardEvent['cal'],
  opts: { time?: string; allDay?: boolean } = {},
): BoardEvent {
  return {
    id: `${iso}|${title}|${cal}`,
    iso,
    title,
    cal,
    source: 'static',
    ...opts,
  };
}

